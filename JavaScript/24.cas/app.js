// Math metode

// Nesto drugacije math metode

// Postoje 4 metode pomocu kojih mozemo da zaokruzimo broj (ucinimo ga cijelim)

// 1.Math.round() zaokruzuje broj najblizem cijelom broju

console.log(Math.round(4.41))

// 2. Math.ceil() na sledeci cio broj

console.log(Math.ceil(4.41))

// 3. Math.floor() zaokruzuje na predhodni cio broj

console.log(Math.floor(4.41))
console.log(Math.floor(-5.7))


// 4. Math.trunc() - zaokruzuje broj tako sto odstrani decimalni zapis

console.log(Math.trunc(4.41))


// Math.sign() - moze vratiti tri vrijednosti

//   -1 ako je argument metode negativan broj
//   0 ako je argument metode 0
//   1 ako je argument metode pozitivan broj

console.log(Math.sign(-89))
console.log(Math.sign(89))
console.log(Math.sign(0))

// Math.pow - vraca prvi argument na drugi argument

console.log(Math.pow(6, 4))

// Math.sqrt() - vraca korijen argumenta

console.log(Math.sqrt(144))

// Math.abs() - apsolutna(pozitivna) vrijednost broja

console.log(Math.abs(-764))

// Math.sin() - sinus tog  broja, argument je u radijanima

console.log(Math.sin(Math.PI/2))

// Math.con() - kosinus tog  broja, argument je u radijanima

console.log(Math.cos(Math.PI))

// Imamo dvije metode za nalazenje najmanje i najveze vrijednosti
// za nekoliko brojeva

// Math.min() Math.max()

console.log(Math.min(-11, 22 , 424, -2123))

console.log(Math.max(-11, 22 , 424, -2123))

// Math.random() vraca br izmedju 0 i 1

console.log(Math.random())

console.log(Math.floor(Math.random() * 10))

console.log(Math.floor(Math.random() * 496) + 5)


// Math.log() metode

// Math.log(x) - vraca logaritam broja x za osnovu e

console.log(Math.log(6))

// Math.log2(x) - vraca logaritam broja x za osnovu 2

console.log(Math.log2(6))

// Math.log2(x) - vraca logaritam broja x za osnovu 10

console.log(Math.log10(6))

